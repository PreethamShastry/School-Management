        <div class="factxt">
           <h1>FACILITY</h1>
           <p> Digital Classroom : The institution has got its own digital classrooms where in the traditional blackboard is replaced by interactive boards and LCD projector installed. Our teachers are well trained by TeachNext to use the facilities.</p>
           <p>Social concern : The Institution prepares each student to become
            socially conscious, ready to serve others and stand for the oppressed. The Institution has adopted poor students with an aim to lend its shoulder to uplift the downtrodden. The Institution reaches out the needy students with Social Concern.</p>
           <p>Character Formation : The Institution helps every student build up
            his character with high values through Leadership seminars, Human Resource Development and Value Education.</p>
           <p>Uplifting the Downtrodden : Institution provides Scholarships and
            Fee concession to the economically weaker students and help them stand on par with others in academic excellence.</p>
           <p>Governing Committee : Ensures each student gets the same
            standardized attention academically, oversees smooth conduct of examinations, healthy rapport between teachers and students and guides the Students in its
            activities.</p>
           <p>Disciplinary Committee : Discipline is maintained in all respects
            starting from the campus discipline, classroom discipline to examination hall discipline. This committee also deals with the examination malpractice by the students both internal and external.</p>
           <p>Grievance Committee : The grievances of students, attendance
            monitoring, and interaction with the parents of students having problems are attended to by the members of this committee.</p>
           <p>Library : As an encouragement of reading habits in primary and
            high school students a school library is installed in our institution with self – sufficient books. A librarian has been appointed to maintain school library.</p>
           <p>Science Lab : Lab is essential to a secondary school to acquire
            practical knowledge on minor science experiments. Hence a spacey science lab with necessary chemicals, materials, charts and other requirements is provided for the students of High school section.</p>
           <p>Physical Education : Physical Education along with health
            education is imparted in our institution. A physical director has been appointed for primary and high school separately. Students are encouraged to take active part in this direction.</p>
           <p>Computer Education: Computer education is provided for students
            from standard V to X. Computer instructor was appointed to impart computer knowledge to the student. Separate syllabus for separate classes is introduced to expand their computer knowledge.</p>
           <p>Transport Facility : School vans are provided for the students
            Transportation, who reside away from Kulai locality.</p>
