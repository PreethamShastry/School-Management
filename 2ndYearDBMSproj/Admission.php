        <h3>ADMISSION :</h3>
        <p>Admission is open for two years course of Pre-Primary Education (Nursery .Section), 7 years course of Primary Education and 3 years course of High School Education.</p>

        <p>The nursery section is a two years course with L.K.G. and U.K.G. Classes.</p>
        <p>First year is spent in correct habit formation, manners, oral training in nursery rhymes and picture talk.</p>
        <p>During the second year the idea of formation of numbers and simple knowledge of the alphabets, notation and numeration are taught.</p>
        <p>The aim of kindergarten course is to equip the pupil to be ready for formal education.</p>
        <p>No Child will be admitted to U.K.G. without the completion of L.K.G. course.</p>
        <p>Only those who have completed 3.6 years are eligible for admission to L.K.G. minimum age for admission to I std. is 5.6 years.</p>
        <p>Application for admission should be made in the prescribed form which can be obtained from the office and should be submitted along with the transfer certificate from the school last attended. Every pupil on admission shall pay the prescribed fees.</p>
        <p>Pupils coming from schools outside the District will be admitted on production of transfer certificates duly countersigned by the B.E.O. in charge of the institutions in strict conformity with the table of parity equivalence of standards issued by the department.</p>
        <p>The application for admission must be signed by the father or mother or in their absence by the guardian who has been authorized to sign.</p>
        <p>In the case of admission of pupils coming from Primary Schools the date of birth furnished in the Transfer Certificate will be taken as correct, in the case of pupils to be newly admitted the correct date of birth should be furnished and necessary documentary evidence be produced if necessary.</p>
        <p>Admissions are made by the admission committee after the personal interview .</p>
        <p>The candidates who do not appear for the interview on the specified date and time will not be considered for admission.</p>
        <p>The Candidates who are selected after the interview should pay the fees immediately.</p>
        <p>The candidate’s who leave the school after admission will forfeit all the charges paid by them. They cannot be refundable</p>
        <p>Monthly fees should be paid by 10th of every month failing which a penalty of  per-day will be levied till the end of the month and thereafter names will be removed from rolls. The child will be re-admitted after the payments of arrears and admission fees.</p>
        <p>General rules and regulations of the school are printed in the dairy and the parents should go through the same at the time of admission.</p>
